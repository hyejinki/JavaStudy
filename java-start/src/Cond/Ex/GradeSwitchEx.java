package Cond.Ex;

public class GradeSwitchEx {

    public static void main(String[] args) {

        String grade = "B";
        System.out.println("grade: " + grade);

        switch (grade) {
            case "A":
                System.out.println("탁월한 성과입니다!");
                break;
            case "B":
                System.out.println("탁월한 성과입니다!");
                break;
            case "C":
                System.out.println("탁월한 성과입니다!");
                break;
        }
    }
}
